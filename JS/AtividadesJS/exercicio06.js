// Converter temperatura de Celsius para Fahrenheit
let celsius = 25;

let fahrenheit = celsius * 1.8 + 32;
console.log("Temperatura em Fahrenheit:", fahrenheit);

// Também adicionei outras converções
// Converter para Kelvin
let kelvin = celsius + 273.15;
console.log("Temperatura em Kelvin:", kelvin);

// Converter para Rankine
let rankine = (celsius + 273.15) * 1.8;
console.log("Temperatura em Rankine:", rankine);
